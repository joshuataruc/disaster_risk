    <ul>
      <li class="">
        Listen to alerts. The Volcano Notification Service provides up-to-date
        information about eruptions.
      </li>
      <li class="">
        Avoid areas downwind, and river valleys downstream, of the volcano.
        Rubble and ash will be carried by wind and gravity.
      </li>
      <li class="">
        Take temporary shelter from volcanic ash where you are if you have
        enough supplies. Cover ventilation openings and seal doors and windows.
        Wear a mask and maintain a distance of at least six feet between
        yourself and those who are not a part of your household to slow the
        spread of COVID-19.
      </li>
      <li class="">
        If outside, protect yourself from falling ash that can irritate skin and
        injure breathing passages, eyes, and open wounds. Use a well-fitting,
        certified face mask such as an N95. The Centers for Disease Control and
        Prevention (CDC) has a list of certified masks and the maker’s
        instructions on how to use the masks. Use a cloth mask to protect
        yourself for shorter periods of time.
      </li>
      <li>Avoid driving in heavy ash fall.</li>
    </ul>
    <br />
    <h3 class="">Be Safe AFTER</h3>
    <ul>
      <li class="">
        Send text messages or use social media to reach out to family and
        friends. Phone systems are often busy after a disaster. Only make
        emergency calls.
      </li>
      <li class="">
        Avoid driving in heavy ash. Driving will stir up volcanic ash that can
        clog engines and stall vehicles.
      </li>
      <li class="">
        If you have any breathing problems, avoid contact with ash. Stay indoors
        until authorities say it is safe to go outside.
      </li>
      <li class="">
        Do not get on your roof to remove ash unless you have guidance or
        training. If you have to remove ash, then be very careful as ash makes
        surfaces slippery. Be careful not to contribute additional weight to an
        overloaded roof.
      </li>
    </ul>
