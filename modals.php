    <h3 class="">Landslide Safety Tips</h3>
    <ul>
        <li>Adhere to local land-use regulations. If you live in an area that is susceptible to mudslides or landslides, make sure your home adheres to all local building codes.</li>
        <li>Put property protections in place. To keep landslides from hitting your home and property, consider adding nets, retaining walls, and planting sturdy vegetation, especially on slopes or in places where wildfire has destroyed plants and trees. Do not remove vegetation that may help prevent erosion.</li>
        <li>Add sandbags in exposed areas. If you need to divert water from pathways or places where a retaining wall or vegetation has given out.</li>
        <li>Do not build your home in places that may be in the path of a landslide. For example, on or at the base of unstable slopes and or minor drainage hollows, at the base or on top of an old fill slope and or a steep-cut slope, or areas that are susceptible to flooding.</li>
        <li>Do your research. It is smart to understand your property’s landscape (i.e. where a landslide path may be) and any history of landslides/mudslides in area. Check out the FEMA Flood Map Service Center to research flood zones and land slippages close to your home.</li>
        <li>Look for new cracks. Check out door and window jambs, walls, brick/masonry, foundations, retaining walls, and tilt of utility poles or trees, as they may be signs of previous landslide and risk of future landslide as well.</li>
    </ul>
    <br>
    <h3 class="">How to stay safe during a landslide</h3>
    <ul>
        <li>If you find yourself in a landslide, you may have only seconds to act. Here’s what to do:</li>
        <li>Go higher. If you are unable to evacuate, move to an upper level of your home and stay alert while you listen to a NOAA weather radio or watch television for weather updates.</li>
        <li>Watch for flowing water. If you are in your vehicle, stay alert and watch for flowing water, which can be a precursor to a landslide.</li>
        <li>Listen. If you are walking or hiking, listen for sounds that might indicate a landslide, including boulders knocking together or trees crackling. You are generally safer away from slopes, so move to higher ground if needed.</li>
    </ul>
    <br>
    <h3 class="">What to do after a landslide</h3>
    <ul>
        <li>Avoid the slide area. Keep in mind that the landslide might not actually be over, so take extra precaution and follow local governing authority.</li>
        <li>Call for help.  If possible, alert authorities of the landslide; they will direct any necessary search and rescue missions for people who may be trapped. </li>
        <li>Check for damage. When it is safe to return home or go outside, check your property for and report any damage.</li>
        <li>Listen to your water company. Pay attention to boil advisories or communications from your local water utility company, as landslides can cause water contamination, making it unsafe to drink or use.</li>
    </ul>
