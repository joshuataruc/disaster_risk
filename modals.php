    <h3 class="">What to do during an earthquake</h3>
    <ul>
      <li class="">
        The steps you should take during an earthquake depend on your location.
      </li>
    </ul>
    <br />
    <h3 class="">If You Are Inside</h3>
    <ul>
      <li class="">
        Stay inside. Do not get in a doorway — this does not provide protection
        from falling or flying objects, and you might not be able to remain
        standing.
      </li>
      <li class="">
        Drop, Cover, and Hold On
        <ul>
          <li>
            <b>Drop</b> down to the ground so the earthquake doesn’t knock you
            down.
          </li>
          <li>
            <b>Cover</b>your head and neck with your arms to protect you from
            falling debris. If possible, crawl under a sturdy desk, table or
            other piece of furniture for additional protection. Stay away from
            glass, windows, outside doors and walls, and other items that could
            fall.
          </li>
          <li>
            <b>Hold On</b> on to any sturdy item you can until the shaking
            stops.
          </li>
        </ul>
      </li>
      <li class="">
        Keep candles at least 12 inches away from anything that can burn. Blow
        them out when you leave the room or before you go to sleep.
      </li>
      <li class="">
        Teach kids never to play with matches and lighters. Make a habit of
        placing these items up and away, out of reach and sight of young
        children.
      </li>
      <li class="">
        Choose the right wood for your fireplace. Burn only dry, seasoned wood
        and avoid burning trash in your fireplace.
      </li>
    </ul>
    <br />
    <h3 class="">If You Are Outside</h3>
    <ul>
      <li class="">
        Move away from buildings, streetlights, and utility wires.
      </li>
    </ul>
    <br />
    <h3 class="">If You Are in a Vehicle</h3>
    <ul>
      <li class="">
        Pull to the side of the road and stay inside until the earthquake is
        over. Do not stop under overpasses or power lines.
      </li>
    </ul>
    <br />
    <h3 class="">What to do after an earthquake strikes</h3>
    <ul>
      <li class="">Continue to monitor media for emergency information.</li>
      <li class="">Follow instructions from public safety officials.</li>
      <li class="">
        If you are trapped, try calling or texting for help. Try tapping on a
        pipe or wall or using a whistle to help rescuers locate you.
      </li>
      <li class="">
        Call 9-1-1 to report emergencies including downed power lines and gas
        leaks.
      </li>
      <li class="">
        Stay away from downed utility wires. Always assume a downed power line
        is live.
      </li>
      <li class="">
        Stay out of damaged buildings and areas until authorities deem them
        safe.
      </li>
      <li class="">
        Check your home for damage:
        <ul>
          <li>
            If you believe there is a gas leak, go outdoors immediately, and do
            not turn electrical switches or appliances on or off.
          </li>
          <li>
            Check chimneys for visual damage and have a professional inspect the
            chimney for internal damage before lighting a fire.
          </li>
          <li>
            If your home or property is damaged, take photos or videos to
            document your damage, and contact your insurance company.
          </li>
        </ul>
      </li>
      <li class="">
        If phone lines are down, use social media or texting to let others know
        you are OK.
      </li>
      <li class="">
        Be a good neighbor. Check on family, friends, and neighbors, especially
        the elderly, those who live alone, those with medical conditions, and
        those who may need additional assistance.
      </li>
    </ul>
