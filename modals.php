      </li>
      <li class="">
        Reduce your ash exposure by doing the following:
        <ul>
          <li>
            Limit your time outdoors and use a dust mask or cloth mask as a last
            resort.
          </li>
          <li>
            Avoid areas downwind and river valleys downstream of the volcano.
          </li>
          <li>Take temporary shelter from volcanic ash where you are.</li>
          <li>Cover ventilation openings and seal doors and windows.</li>
          <li>
            Avoid driving in heavy ash. If you must drive, keep the windows up
            and do not use the air conditioning system.
          </li>
          <li>Do not get on your roof to remove ash.</li>
          <li>
            If you have any breathing problems, avoid contact with ash and stay
            indoors until authorities say it is safe to go outside.
          </li>
        </ul>
      </li>
    </ul>
    <br />
    <h3 class="">Prepare NOW</h3>
    <ul>
      <li class="">Know your area’s risk from volcanic eruption.</li>
      <li class="">
        Ask local emergency management for evacuation and shelter plans, and for
        potential means of protection from ash.
      </li>
      <li class="">
        Learn about community warning systems by signing up for a free service
