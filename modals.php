    <br />
    <h3 class="">During a flood</h3>
    <ul>
      <li class="">
        Listen to your radio or TV for emergency information. Evacuate
        immediately if told to do so.
      </li>
      <li class="">
        Do not walk through flowing water. Drowning is the number one cause of
        flood deaths, mostly during flash floods. Currents can be deceptive; six
        inches of moving water can knock you off your feet. If you walk in
        standing water, use a pole or stick to ensure that the ground is still
        there.
      </li>
      <li class="">
        Do not drive through a flooded area. More people drown in their cars
        than anywhere else. Do not drive around road barriers; the road or
        bridge may be washed out.
      </li>
      <li class="">
        File your flood claim. To file a claim, you will need your insurance
        company's name, your policy number, and a number where you can be
        reached. Take photos of any water in the house and anything damaged in
        your home. Make a detailed list of all damaged or lost items.
      </li>
