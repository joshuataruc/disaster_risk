    <h3 class="">Be Aware of Fire Hazards in Your Home</h3>
    <ul>
      <li class="">
        Limit distractions when cooking. Don’t leave a hot oven or stovetop
        unattended. Keep anything that can catch fire, such as dish towels or
        wooden spoons, away from your stovetop.
      </li>
      <li class="">
        Unplug small appliances when not in use. Make sure that you unplug
        appliances like toasters, toaster ovens and slow cookers when you are
        not using them.
      </li>
      <li class="">
        Keep candles at least 12 inches away from anything that can burn. Blow
        them out when you leave the room or before you go to sleep.
      </li>
      <li class="">
        Teach kids never to play with matches and lighters. Make a habit of
        placing these items up and away, out of reach and sight of young
        children.
      </li>
      <li class="">
        Choose the right wood for your fireplace. Burn only dry, seasoned wood
        and avoid burning trash in your fireplace.
      </li>
    </ul>
    <br />
    <h3 class="">How to Create and Practice a Fire Escape Plan</h3>
    <ul>
      <li class="">
        Create and practice a fire escape plan. Create a home fire escape plan
        with two ways out of every room (ex. a window and a door). Choose a
        place to meet outside that is a safe distance away from your home.
      </li>
      <li class="">
        Practice a home fire drill at least twice a year. Practice a home fire
        drill with your family during the day and at night. Practice escaping in
        under two minutes. Sometimes that is all the time you will have to get
        out of the house safely.
      </li>
      <li class="">
        Choose one person to be responsible for getting infants and young
        children out of the home. Have a back-up person to help young children
        just in case the primary person is unable to help.
      </li>
      <li class="">
        Teach older children how to follow your fire escape plan. Make sure they
        know to “get low and go” if there is smoke when they are leaving the
        home. Teach them to open windows, remove screens and unlock doors if
        necessary.
      </li>
    </ul>
    <br />
    <h3 class="">In an Emergency, Leave Home Immediately</h3>
    <ul>
      <li class="">
        If there is a fire, leave home immediately. If there is a lot of smoke,
        get low and crawl out of the house as quickly as possible. Call 911
        after you are a safe distance away from your home. Stay in the safe
        location until emergency personnel tell you it is safe to re-enter your
        home.
      </li>
      <li class="">
        Watch for flowing water. If you are in your vehicle, stay alert and
        watch for flowing water, which can be a precursor to a landslide.
      </li>
      <li class="">
        Listen. If you are walking or hiking, listen for sounds that might
        indicate a landslide, including boulders knocking together or trees
        crackling. You are generally safer away from slopes, so move to higher
        ground if needed.
      </li>
